import numpy as np

# Generating arrays with specific values
zeros_arr = np.zeros((2, 3))  # Array of zeros
ones_arr = np.ones((2, 3))    # Array of ones
identity_matrix = np.eye(3)    # Identity matrix
random_arr = np.random.random((2, 2))  # Random array

print("Zeros Array:")
print(zeros_arr)

print("\nOnes Array:")
print(ones_arr)

print("\nIdentity Matrix:")
print(identity_matrix)

print("\nRandom Array:")
print(random_arr)
